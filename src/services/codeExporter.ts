import { CommandBlock, CommandType, AppMode } from '../types';

export const exportToPython = (commands: CommandBlock[], mode: AppMode): string => {
  let code = "# Generated by KidCode Studio\n";
  code += "import kidcode\n\n";

  if (mode === AppMode.GAME) {
    code += "game = kidcode.Game()\n";
    code += "player = game.player\n\n";
  } else if (mode === AppMode.HARDWARE) {
    code += "board = kidcode.Hardware()\n\n";
  } else if (mode === AppMode.APP) {
    code += "app = kidcode.App()\n\n";
  }

  let indent = "";

  commands.forEach(cmd => {
    switch (cmd.type) {
      // === GAME COMMANDS ===
      case CommandType.MOVE_X:
        code += `${indent}player.move_x(${cmd.params.value || 10})\n`;
        break;
      case CommandType.MOVE_Y:
        code += `${indent}player.move_y(${cmd.params.value || 10})\n`;
        break;
      case CommandType.JUMP:
        code += `${indent}player.jump(${cmd.params.value || 10})\n`;
        break;
      case CommandType.SET_GRAVITY:
        code += `${indent}player.gravity = ${cmd.params.condition === 'true'}\n`;
        break;
      case CommandType.SHOOT:
        code += `${indent}player.shoot("${cmd.params.text || 'bullet'}")\n`;
        break;
      case CommandType.SET_SCENE:
        code += `${indent}game.set_scene("${cmd.params.text || 'default'}")\n`;
        break;
      case CommandType.SET_SCORE:
        code += `${indent}game.score = ${cmd.params.value || 0}\n`;
        break;
      case CommandType.CHANGE_SCORE:
        code += `${indent}game.score += ${cmd.params.value || 1}\n`;
        break;
      case CommandType.GAME_OVER:
        code += `${indent}game.game_over()\n`;
        break;
      case CommandType.WIN_GAME:
        code += `${indent}game.win()\n`;
        break;

      // === APP COMMANDS ===
      case CommandType.SET_TITLE:
        code += `${indent}app.set_title("${cmd.params.text || 'My App'}")\n`;
        break;
      case CommandType.ADD_BUTTON:
        code += `${indent}app.add_button("${cmd.params.text || 'Button'}", lambda: print("${cmd.params.message || 'clicked'}"))\n`;
        break;
      case CommandType.ADD_INPUT:
        code += `${indent}app.add_input("${cmd.params.text || 'Enter text'}", "${cmd.params.varName || 'var1'}")\n`;
        break;
      case CommandType.ADD_TEXT_BLOCK:
        code += `${indent}app.add_text("${cmd.params.text || 'Hello'}")\n`;
        break;
      case CommandType.SHOW_ALERT:
        code += `${indent}app.show_alert("${cmd.params.text || 'Alert!'}")\n`;
        break;
      case CommandType.NAVIGATE:
        code += `${indent}app.navigate_to("${cmd.params.text || 'main'}")\n`;
        break;

      // === HARDWARE COMMANDS ===
      case CommandType.LED_ON:
        code += `${indent}board.pin(${cmd.params.pin || 13}).on()\n`;
        break;
      case CommandType.LED_OFF:
        code += `${indent}board.pin(${cmd.params.pin || 13}).off()\n`;
        break;
      case CommandType.LED_TOGGLE:
        code += `${indent}board.pin(${cmd.params.pin || 13}).toggle()\n`;
        break;
      case CommandType.SET_SERVO:
        code += `${indent}board.servo(${cmd.params.pin || 9}).write(${cmd.params.angle || 90})\n`;
        break;
      case CommandType.SET_FAN:
        code += `${indent}board.fan(${cmd.params.pin || 9}).set_speed(${cmd.params.speed || 50})\n`;
        break;
      case CommandType.READ_DIGITAL:
        code += `${indent}${cmd.params.varName || 'value'} = board.pin(${cmd.params.pin || 4}).read()\n`;
        break;
      case CommandType.READ_ANALOG:
        code += `${indent}${cmd.params.varName || 'value'} = board.pin(${cmd.params.pin || 97}).read_analog()\n`;
        break;

      // === CONTROL FLOW ===
      case CommandType.WAIT:
        code += `${indent}kidcode.wait(${cmd.params.value || 1})\n`;
        break;
      case CommandType.FOREVER:
        code += `${indent}while True:\n`;
        indent += "    ";
        break;
      case CommandType.END_FOREVER:
        indent = indent.slice(0, -4);
        break;
      case CommandType.REPEAT:
        code += `${indent}for i in range(${cmd.params.value || 1}):\n`;
        indent += "    ";
        break;
      case CommandType.END_REPEAT:
        indent = indent.slice(0, -4);
        break;
      case CommandType.IF:
        code += `${indent}if ${cmd.params.condition || 'True'}:\n`;
        indent += "    ";
        break;
      case CommandType.ELSE:
        indent = indent.slice(0, -4);
        code += `${indent}else:\n`;
        indent += "    ";
        break;
      case CommandType.END_IF:
        indent = indent.slice(0, -4);
        break;

      // === VARIABLES & MATH ===
      case CommandType.SET_VAR:
        const strVal = typeof cmd.params.value === 'string' ? `'${cmd.params.value}'` : cmd.params.value;
        code += `${indent}${cmd.params.varName || 'var'} = ${strVal}\n`;
        break;
      case CommandType.CHANGE_VAR:
        code += `${indent}${cmd.params.varName || 'var'} += ${cmd.params.value || 1}\n`;
        break;
      case CommandType.CALC_ADD:
        code += `${indent}${cmd.params.varName || 'result'} = ${cmd.params.value || 0} + ${cmd.params.value2 || 0}\n`;
        break;
      case CommandType.CALC_SUB:
        code += `${indent}${cmd.params.varName || 'result'} = ${cmd.params.value || 0} - ${cmd.params.value2 || 0}\n`;
        break;
      case CommandType.CALC_MUL:
        code += `${indent}${cmd.params.varName || 'result'} = ${cmd.params.value || 0} * ${cmd.params.value2 || 0}\n`;
        break;
      case CommandType.CALC_DIV:
        code += `${indent}${cmd.params.varName || 'result'} = ${cmd.params.value || 0} / ${cmd.params.value2 || 0}\n`;
        break;
      case CommandType.CALC_RANDOM:
        code += `${indent}${cmd.params.varName || 'rand'} = random.randint(${cmd.params.value || 1}, ${cmd.params.value2 || 10})\n`;
        break;

      // === SOUND ===
      case CommandType.PLAY_TONE:
        code += `${indent}kidcode.play_tone(${cmd.params.duration || 0.5})\n`;
        break;
      case CommandType.PLAY_SOUND:
        code += `${indent}kidcode.play_sound("${cmd.params.text || 'beep'}")\n`;
        break;

      // === DEFAULT ===
      default:
        code += `${indent}# Block: ${cmd.type} (not yet supported in Python export)\n`;
    }
  });

  return code;
};

