// NEW: Arduino/C++ Export
export const exportToArduino = (commands: CommandBlock[]): string => {
  let code = "/* Generated by KidCode Studio */\n";
  code += "// Arduino Sketch - Upload to your board using Arduino IDE\n\n";

  // Collect used pins
  const usedPins = new Set<number>();
  commands.forEach(cmd => {
    if (cmd.params.pin !== undefined) usedPins.add(cmd.params.pin);
  });

  // Setup section
  code += "void setup() {\n";
  code += "  Serial.begin(9600);\n";
  usedPins.forEach(pin => {
    code += `  pinMode(${pin}, OUTPUT);\n`;
  });
  code += "}\n\n";

  // Loop section
  code += "void loop() {\n";

  commands.forEach(cmd => {
    switch (cmd.type) {
      case CommandType.LED_ON:
        code += `  digitalWrite(${cmd.params.pin || 13}, HIGH);\n`;
        break;
      case CommandType.LED_OFF:
        code += `  digitalWrite(${cmd.params.pin || 13}, LOW);\n`;
        break;
      case CommandType.LED_TOGGLE:
        code += `  digitalWrite(${cmd.params.pin || 13}, !digitalRead(${cmd.params.pin || 13}));\n`;
        break;
      case CommandType.WAIT:
        code += `  delay(${(cmd.params.value || 1) * 1000});\n`;
        break;
      case CommandType.SET_SERVO:
        code += `  // Servo on pin ${cmd.params.pin || 9}: angle = ${cmd.params.angle || 90}\n`;
        break;
      case CommandType.READ_DIGITAL:
        code += `  int ${cmd.params.varName || 'value'} = digitalRead(${cmd.params.pin || 4});\n`;
        break;
      case CommandType.READ_ANALOG:
        code += `  int ${cmd.params.varName || 'value'} = analogRead(${cmd.params.pin || 97});\n`;
        break;
      case CommandType.PLAY_TONE:
        code += `  tone(8, 440, ${(cmd.params.duration || 0.5) * 1000});\n`;
        break;
      default:
        code += `  // ${cmd.type} - not supported in Arduino\n`;
    }
  });

  code += "\n  delay(100); // Small delay to prevent runaway\n";
  code += "}\n";

  return code;
};

